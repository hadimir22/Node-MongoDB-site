$( document ).ready(function() { 



    $(window).scroll(function(){
        var scroll =  $(window).scrollTop();
         if(scroll >= 90){ 
             $(".nav").css("background-color", "black");
         }
         else {
             $(".nav").css("background-color", "transparent");
         }
      });
     
   
    
     
 


   });
    
 

   
    
    
    
    
    
    
     
    
    